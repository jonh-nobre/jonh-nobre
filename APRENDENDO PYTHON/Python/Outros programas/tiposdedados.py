# Inteiro(int)

idade = 18
ano = 2002

print(type(idade))
print(type(ano))

#Ponto Flutuante ou Decimal(float)

altura = 1.80
peso = 73.55

print(type(altura))
print(type(peso))

#Complexo(complex)

a = 5+2j
b = 20+6j

print(type(a))
print(type(b))
print(complex(2,5))

# String(str)

nome = 'Guilherme'
profissao = 'Engenheiro de Software'

print(type(nome))
print(type(profissao))

# Boolean(bool)

fim_de_semana = True
feriado = False

print(type(fim_de_semana))
print(type(feriado))

# Listas(list)

alunos = ['Amanda', 'Ana', 'Bruno', 'João']
notas = [10, 8.5, 7.8, 8.0]

print(type(alunos))
print(type(notas))

# Tuplas(tuples)

valores = (90, 79, 54, 32, 21)
pontos = (100, 94,.05, 86.8, 62)

print(type(valores))
print(type(pontos))

# Dicionários(dict)

altura = {'Amanda': 1.65, 'Ana': 1.60, 'João': 1.70}
peso = {'Amanda':60, 'Ana':58, 'João': 68}

print(type(altura))
print(type(peso))
